CREATE TABLE OFFICE(
    ID_OFFICE int IDENTITY (1, 1) NOT NULL,
    NAME_OFFICE varchar(50) NOT NULL,
    DIR_OFFICE varchar(150) NOT NULL,
    PHONE_OFFICE varchar(15) NOT NULL,
    CONSTRAINT PK_OFFICE PRIMARY KEY (ID_OFFICE)
);
CREATE TABLE USER_OFFICE(
    ID_USER int IDENTITY(1, 1) NOT NULL,
    NAME_USER varchar(100) NOT NULL,
    LAST_USER varchar(100) NOT NULL,
    USER_USER varchar(50) NOT NULL,
    PASSWORD_USER varchar(32) NOT NULL,
    ID_OFFICE_USER int NOT NULL,
    CONSTRAINT PK_USER PRIMARY KEY (ID_USER),
    CONSTRAINT FK_ID_OFFICE_USER FOREIGN KEY (ID_OFFICE_USER) REFERENCES OFFICE(ID_OFFICE) ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE OFFICE_DETAIL(
    ID_DETAIL int IDENTITY(1, 1) NOT NULL,
    DATE_DETAIL datetime NOT NULL,
    ID_USER_DETAIL int NOT NULL,
    LOCATION_DETAIL varchar(200) NOT NULL,
    CONSTRAINT PK_OFFICE_DETAIL PRIMARY KEY(ID_DETAIL),
    CONSTRAINT FK_USER_DETAIL FOREIGN KEY (ID_USER_DETAIL) REFERENCES USER_OFFICE(ID_USER) ON DELETE CASCADE ON UPDATE CASCADE
)